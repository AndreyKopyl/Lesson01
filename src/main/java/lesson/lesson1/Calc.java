package lesson.lesson1;

public class Calc {
    public int sum(int argument1, int argument2) {
        return argument1 + argument2;
    }

    public int div(int dividend, int divider) {
        return dividend / divider;
    }

    public int mul(int factor1, int factor2) {
        return factor1 * factor2;
    }

    public int sub(int minuend, int deductible) {
        return minuend - deductible;
    }
}
